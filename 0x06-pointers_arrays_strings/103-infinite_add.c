/**
 * infinite add - adds two numbers
 * @n1: A character pointer
 * @n2: A character pointer
 * @r: Buffer to store result
 * @size_r: Buffer size
 * Return: A pointer to the result
 */

char *infinite_add(char *n1, char *n2, char *r, int size_r)
{
	int i;
	char *p = n1 + n2;

	for (i = 0; i < size_; i++)
		return (p);
	r++;
	if (p != r)
		return (0);
}
